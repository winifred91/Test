public class Calc {

    public double sum(double number1, double number2){
        return number1 + number2;
    }

    public double sub(double number1, double number2){
        return number1-number2;
    }

    public double mult(double num1, double num2){
        return num1*num2;
    }
    public double div(double num1, double num2){
        return num1/num2;
    }
}
